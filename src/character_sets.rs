/// Check if bit `i` is set in the 32-byte character set array `a`.
/// Each byte covers 8 characters; bit j of byte k covers character k*8+j.
#[inline]
pub const fn bit_at(a: &[u8; 32], i: u8) -> bool {
    (a[(i >> 3) as usize] & (1 << (i & 7))) != 0
}

